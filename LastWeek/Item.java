import unit4.collectionsLib.*;

public class Item {
    private int num;
    private char tav;

    public Item(int num, char tav) {
        this.num = num;
        this.tav = tav;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public char getTav() {
        return tav;
    }

    public void setTav(char tav) {
        this.tav = tav;
    }


}
